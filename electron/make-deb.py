import os
import subprocess
import shutil
import tarfile

APP_NAME = "ubuntu-toolbox-desktop"
DISPLAY_NAME = "Ubuntu 工具箱"
VERSION = "1.0.0"
MAINTAINER = "yangsir <yangsir@example.com>"
DESC = "Ubuntu 工具箱桌面版 - 跨平台桌面应用"
CATEGORY = "Utility"
HOMEPAGE = "https://github.com/yangsir/ubuntu-toolbox"

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
UNPACKED = os.path.join(SCRIPT_DIR, "dist", "linux-unpacked")
DEB_DIR = os.path.join(SCRIPT_DIR, "dist", "deb_root")
DEST_DEB = os.path.join(SCRIPT_DIR, "dist", f"{APP_NAME}_{VERSION}_amd64.deb")

os.makedirs(DEB_DIR, exist_ok=True)
os.makedirs(os.path.join(DEB_DIR, "usr", "bin"), exist_ok=True)
os.makedirs(os.path.join(DEB_DIR, "usr", "share", "applications"), exist_ok=True)
os.makedirs(os.path.join(DEB_DIR, "usr", "share", "icons", "hicolor", "256x256", "apps"), exist_ok=True)

# 复制可执行文件和资源
shutil.copy(os.path.join(UNPACKED, APP_NAME), os.path.join(DEB_DIR, "usr", "bin", APP_NAME))
shutil.copy(os.path.join(UNPACKED, "chrome-sandbox"), os.path.join(DEB_DIR, "usr", "bin", "chrome-sandbox"))

# 复制整个 resources 目录
shutil.copytree(
    os.path.join(UNPACKED, "resources"),
    os.path.join(DEB_DIR, "usr", "share", APP_NAME, "resources"),
    dirs_exist_ok=True
)

# 复制 locles 和 .pak 文件
for item in os.listdir(UNPACKED):
    if item.endswith(".pak") or item.endswith(".bin") or item in ["icudtl.dat"]:
        shutil.copy(os.path.join(UNPACKED, item), os.path.join(DEB_DIR, "usr", "share", APP_NAME, item))
    if item == "locales":
        shutil.copytree(os.path.join(UNPACKED, item), os.path.join(DEB_DIR, "usr", "share", APP_NAME, item), dirs_exist_ok=True)
    if item.startswith("lib"):
        shutil.copy(os.path.join(UNPACKED, item), os.path.join(DEB_DIR, "usr", "share", APP_NAME, item))

# 修复 chrome-sandbox 权限
os.chmod(os.path.join(DEB_DIR, "usr", "bin", "chrome-sandbox"), 0o4755)

# 创建 desktop 文件
desktop = f"""[Desktop Entry]
Name={DISPLAY_NAME}
Comment={DESC}
Exec=/usr/bin/{APP_NAME} %U
Icon={APP_NAME}
Type=Application
Terminal=false
Categories={CATEGORY};
StartupNotify=true
MimeType=x-scheme-handler/ubuntutoolbox;
"""

with open(os.path.join(DEB_DIR, "usr", "share", "applications", f"{APP_NAME}.desktop"), "w") as f:
    f.write(desktop)

